                      return Container();
                    }
                    final buttonBooksRecord = buttonBooksRecordList.first;
                    return FFButtonWidget(
                      onPressed: () async {
                        final issuedBookCreateData = createIssuedBookRecordData(
                          bookRef: buttonBooksRecord.reference,
                          issuedId: random_data.randomString(
                            10,
                            10,
                            true,
                            true,
                            true,
                          ),
                          timeCreated: getCurrentTimestamp,
                          enrollmentNo: textController1!.text,
